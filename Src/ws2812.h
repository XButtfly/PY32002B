/**
 ******************************************************************************
 * @file    ws2812.h
 * @brief   WS2812B 灯带驱动 — PY32F002Bx5 @ 24MHz HSI, GPIO 位脉冲方式
 * @note    PA1 → WS2812B DIN,  5 颗 LED,  GRB 颜色排列
 ******************************************************************************
 */

#ifndef WS2812_H
#define WS2812_H

#include "main.h"

/*===========================================================================
  硬件引脚定义
 *===========================================================================*/
#define WS2812_PIN     LL_GPIO_PIN_1        /* 数据引脚: PA1                    */
#define WS2812_COUNT   5                    /* LED 数量                        */

/*===========================================================================
  颜色常量 — GRB 打包格式: 绿[23:16] | 红[15:8] | 蓝[7:0]
 *===========================================================================*/
#define WS_RED         0x00FF00U            /* 红色                            */
#define WS_GREEN       0xFF0000U            /* 绿色                            */
#define WS_BLUE        0x0000FFU            /* 蓝色                            */
#define WS_WHITE       0xFFFFFFU            /* 白色                            */
#define WS_DIM_GREEN   0x320000U            /* 暗绿色 (G=50, ~20% 亮度)        */
#define WS_OFF         0x000000U            /* 熄灭                            */

/*===========================================================================
  函数声明
 *===========================================================================*/
void ws2812_init(void);                     /* 初始化 GPIO, 发送复位脉冲       */
void ws2812_send(const uint32_t grb[], uint32_t n); /* 发送 n 颗 LED 的 GRB 数据 */

#endif
